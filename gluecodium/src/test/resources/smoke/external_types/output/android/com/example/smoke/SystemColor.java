package com.example.smoke;
final class SystemColor {
    public float red;
    public float green;
    public float blue;
    public float alpha;
    SystemColor(final float red, final float green, final float blue, final float alpha) {
        this.red = red;
        this.green = green;
        this.blue = blue;
        this.alpha = alpha;
    }
}
